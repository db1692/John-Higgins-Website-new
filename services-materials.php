      <h2 class="services-titles">Research Projects</h2>

      <p class="services-text">
        In cross-organisational and organisational specific settings I work with a wide range of research techniques to co-develop original insights into individual and collective experience – opening up the possibility for
        different forms of action.<br><br>
        The informing research philosophy is one that does not distinguish between analysis and action, seeing the process in which collective understanding is developed as fundamental to enabling generative change (or preservation of the status quo)
      </p>

      <hr class="hr-green">

      <h2 class="services-titles">Editorial Support</h2>

