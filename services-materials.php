        <li class="services-list">Shaping ideas for theses, articles and books.</li>
        <li class="services-list">Support in finding authentic, enlivening expression for personal websites.</li>
        <li class="services-list">Direct and indirect wordsmithing for people writing in English as a first or second language.</li>
        <li class="services-list">Being a listening ear so people get a chance to hear what it is they are saying.</li>
        <br>
      </ul>

      <hr class="hr-green">

      <h2 class="services-titles">Thinking partnership</h2>

      <p class="services-text">
        On a one-to-one basis, I offer a conversation between equals that offers quality space and time to explore your presence in organisational life. This is distinctive from advice giving or coaching as it is predicated on
        the two of us coming alongside each other as peers, both curious about what is going on in our respective worlds and noticing how our different experiences do and don’t match. <br><br>

        The focus of these exchanges reflects the nature of your situation and my areas of expertise, so will all but certainly encompass: voice, silence and power in the workplace; engaging generatively with your own activism and the activism of
        others; and developing reflexive curiosity about how and why the two of us see the world the way we do.
      </p>

      <hr class="hr-green">

      <h2 class="services-titles">Personal advice</h2>

      <p class="services-text">I take part in one-off and recurring conversations with people seeking to make sense of their life – be that in terms of a specific issue they are looking to address at work or a more existential inquiry into what role
        they want work to play in their lives. <br><br>

        My approach varies from person to person and is informed by my years in Jungian analysis, Masters in Organisational Change and experience of making significant life choices. While this work largely involves listening to people and playing
        back what I hear, I am probably more willing to be voluble and prescriptive than traditional coaching models advocate.
      </p>

